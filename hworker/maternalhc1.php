   <p align="justify">Care of the mother(and the new born) after delivery is known as post-natal care.</p>
  <p align="justify">As 60% of infant death takes place during 1st month of life, care of mother during and immediately after delivery is vital for safety of mother and as well as infants.</p>
  <p align="justify">The lady health workers, ASHA, TBAs are oriented to visit the mother to check up the health of mother and baby, breast feeding of infant, immunisation.</p>
  <p align="justify">In case some risk factors are identified the mother/infant is referred to public health institution for treatment.
                    There is provision of Rs.150/- per case as cash assistance for referral of sick neonate under RCH programme</p><br><br>
  <p align="justify"><ul align="justify">OBJECTIVES:<br><br>
    <li>To prevent cmplications of the postnatal period.</li>
    <li>To provide care for the rapid restoration of the mother to optimum health.</li>
    <li>To check adequacy of brast feeding.</li>
    <li>To provide basic health education to mother/family.</li>
    <li>To provide care to mother and baby.</li></p><br>
