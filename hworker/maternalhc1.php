  <p align="justify">
    INTRANATAL CARE:<br><br>
    1. Domicillary Care:<br><br>
           Mother with normal obstetric history may be advised to have their confinement in their homes, provided the home conditions are satisfactory. In such cases the delivery may be conducted by the "health worker female or trained dai" this is known as "domiciliary midwifery service".<br><br>
      Advantages of DOMICILLARY service:<br>
        -Mother delivers in the familiar surroundings of her home.<br>
        -Less chance of cross infection.<br>
        -Mother is able to keep an eye upon her children and domestic affairs.<br><br>
      Disadvantages:<br>
        -Mother may have less medical and nursing supervision<br>
        -Mother may have less rest<br>
        -Mother resume her duties too soon<br>
        -Diet may be neglected<br><br>
    2. Institutional Care:<br><br>
            At about 1% of deliveries tend to be abnormal, requiring the services of a doctor institutional care is recommended for all 'high risk' cases and where home conditions are unsuitable.<br><br>
    2. Rooming In:<br><br>
            Keeping the baby's crib the side of the mother's bed is called "rooming-in".<br>
            It also allays the fear in the mother mind that the baby is not misplaced in central nursery.<br>
  </p>
