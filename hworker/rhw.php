      <p>A. Antenatal care:-<br><br>
       i. Contact: Contacting every pregnant mother in the primary stage of pregnancy.<br>
       ii. History: Taking history of general health, previous child birth and present pregnancy.<br>
       iii. Antenatal Examination: Conducting physical birth and pregnancy.<br> 
                                   Identifying high risk of mothers.<br>
                                   Providing counselling and health education.<br>
                                   Helping mother and other family members in planning the delivery.</p><br>
     <ul>B. Intranatal care:-<br>
     	<li>taking mothers to the near health centre for delivery.</li>
     	<li>Giving mental support to mothers.</li>
     	<li>Preparing mother for delivery.</li>
     </ul><br>

       <p>C. Postnatal care:-<br><br>
          -Observing the blood pressure, temperature and pulse of mother after delivery and during the following period.<br>
          -Collecting information about the general condition of mother, food, sleep. pain and elimination etc.<br>

          Home Visit: During home visit, health worker should try to focus the attention of mother on the following points:-<br>
          antenatal check up and its importance.<br>
          diet during pregnancy.<br>
          plans foe delivery.<br>
          neonatal care.</p>
